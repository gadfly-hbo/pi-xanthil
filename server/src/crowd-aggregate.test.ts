test("importAggregateDataset loads demo-aggregate.csv fixture", () => {
  const csvPath = join(process.cwd(), "server/static/crowd/demo-aggregate.csv");
  let csv: string;
  try {
    csv = readFileSync(csvPath, "utf-8");
  } catch {
    return; // skip if file not found (CI)
  }
  const lines = csv.trim().split("\n").filter((l) => l.trim());
  const columns = lines[0]!.split(",").map((c) => c.trim().replace(/^\uFEFF/, ""));
  const rows = lines.slice(1).map((line) => {
    const vals = line.split(",");
    const row: Record<string, unknown> = {};
    for (let i = 0; i < columns.length; i++) {
      const v = vals[i]?.trim() ?? "";
      row[columns[i]!] = isNaN(Number(v)) ? v : Number(v);
    }
    return row;
  });

  const result = importAggregateDataset(workspace.id, "demo-aggregate", columns, rows);
  assert.equal(result.segments.length, 6, "demo has 6 segments");
  assert.equal(result.segments[0]!.name, "都市新锐女性");
  assert.equal(result.segments[5]!.name, "家庭采购主力");
  assert.equal(result.dataset.fieldCount, 26);
});

