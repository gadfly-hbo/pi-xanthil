function parseCsv(filePath: string): { columns: string[]; rows: Record<string, unknown>[] } {
  const csv = readFileSync(filePath, "utf-8");
  const lines = csv.trim().split("\n").filter((l) => l.trim());
  const columns = lines[0]!.split(",").map((c) => c.trim().replace(/^\uFEFF/, ""));
  const rows = lines.slice(1).map((line) => {
    const vals = line.split(",");
    const row: Record<string, unknown> = {};
    for (let i = 0; i < columns.length; i++) {
      const v = vals[i]?.trim() ?? "";
      row[columns[i]!] = isNaN(Number(v)) || v === "" ? v : Number(v);
    }
    return row;
  });
  return { columns, rows };
}

test("X-CROWD12 · D-CROWD10 模板文件契约：4 文件均存在 + BOM + 列结构", () => {
  const root = CROWD_STATIC_ROOT;
  for (const name of ["template-detail.csv", "template-aggregate.csv", "demo-detail.csv", "demo-aggregate.csv"]) {
    const p = join(root, name);
    assert.ok(existsSync(p), `${name} exists`);
    const buf = readFileSync(p);
    assert.equal(buf[0], 0xef, `${name} BOM byte 0`);
    assert.equal(buf[1], 0xbb, `${name} BOM byte 1`);
    assert.equal(buf[2], 0xbf, `${name} BOM byte 2`);
  }

  const detail = parseCsv(join(root, "template-detail.csv"));
  assert.deepEqual(detail.columns, [
    "gender", "age_bracket", "city_tier", "avg_monthly_spend", "price_sensitivity_level",
    "interest_top3", "preferred_channel", "discount_response", "lifecycle_stage", "scenario_top2",
  ]);

  const agg = parseCsv(join(root, "template-aggregate.csv"));
  assert.equal(agg.columns.length, 28, "aggregate 28 cols (segment_name + 26 data + sample_count)");
  assert.equal(agg.columns[0], "segment_name");
  assert.equal(agg.columns[agg.columns.length - 1]!, "sample_count");
});

