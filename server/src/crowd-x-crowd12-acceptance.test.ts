test("X-CROWD12 · E-CROWD11 R5 归一化层接入：demo-detail.csv → 入库英文枚举", () => {
  const { columns, rows } = parseCsv(join(CROWD_STATIC_ROOT, "demo-detail.csv"));
  const fp = computeFieldProfiles({ columns, rows, normalizeEnums: true });

  const gender = fp.find((p) => p.field === "gender")!;
  for (const tv of gender.topValues) {
    assert.ok(["F", "M", "O"].includes(tv.value), `gender 入库英文: ${tv.value}`);
  }

  const channel = fp.find((p) => p.field === "preferred_channel")!;
  for (const tv of channel.topValues) {
    assert.ok(/^[a-z_]+$/.test(tv.value), `channel 入库英文: ${tv.value}`);
  }

  const lifecycle = fp.find((p) => p.field === "lifecycle_stage")!;
  for (const tv of lifecycle.topValues) {
    assert.ok(["new", "active", "repeat", "dormant", "churned"].includes(tv.value), `lifecycle: ${tv.value}`);
  }
});

