test("computeTagDetailFieldProfiles applies type-level top rules and ignores tgi", () => {
  const rows = [
    { 标签类型: "电商品类", 标签: "裤装", 占比: "37.4%", tgi: 999 },
    { 标签类型: "电商品类", 标签: "T恤", 占比: "20%", tgi: 1 },
    { 标签类型: "电商品类", 标签: "卫衣", 占比: "10%", tgi: 1 },
    { 标签类型: "电商品类", 标签: "外套", 占比: "5%", tgi: 1 },
    { 标签类型: "城市", 标签: "二线城市", 占比: "12.4%", tgi: 888 },
    { 标签类型: "城市", 标签: "一线城市", 占比: "8%", tgi: 1 },
  ];
  const profiles = computeTagDetailFieldProfiles({ columns: ["标签类型", "标签", "占比", "tgi"], rows });
  assert.ok(profiles);
  const category = profiles.find((profile) => profile.field === "电商品类");
  const city = profiles.find((profile) => profile.field === "城市");
  assert.deepEqual(category?.topValues.map((value) => value.value), ["裤装", "T恤", "卫衣"]);
  assert.deepEqual(city?.topValues.map((value) => value.value), ["二线城市"]);
  assert.equal(category?.topValues[0]?.ratio, 0.374);
});

test("crowdFieldProfilesToLlmAggregateCsv exports only aggregate LLM input columns", () => {
  const rows = [
    { 标签类型: "电商品类", 标签: "裤装", 占比: "37.4%", tgi: 999 },
    { 标签类型: "电商品类", 标签: "T恤", 占比: "20%", tgi: 1 },
    { 标签类型: "电商品类", 标签: "卫衣", 占比: "10%", tgi: 1 },
    { 标签类型: "电商品类", 标签: "外套", 占比: "5%", tgi: 1 },
    { 标签类型: "城市", 标签: "二线城市", 占比: "12.4%", tgi: 888 },
    { 标签类型: "城市", 标签: "一线城市", 占比: "8%", tgi: 1 },
  ];
  const profiles = computeTagDetailFieldProfiles({ columns: ["标签类型", "标签", "占比", "tgi"], rows });
  assert.ok(profiles);
  const csv = crowdFieldProfilesToLlmAggregateCsv(profiles);
  assert.equal(csv.split("\n")[0], "标签类型,标签,占比");
  assert.match(csv, /电商品类,裤装,37.4%/);
  assert.match(csv, /城市,二线城市,12.4%/);
  assert.doesNotMatch(csv, /tgi/i);
  assert.doesNotMatch(csv, /外套/);
});

