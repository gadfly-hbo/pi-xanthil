test("X-CROWD12 · D-CROWD11 明细 4 步链路 + 安全审计 prompt 不含 raw rows", async () => {
  const workspace = db.createWorkspace("x-crowd12-detail");
  const { columns, rows } = parseCsv(join(CROWD_STATIC_ROOT, "demo-detail.csv"));

  const fp = computeFieldProfiles({ columns, rows, normalizeEnums: true });
  const dataset = data.createCrowdDataset(workspace.id, {
    name: "demo-detail", source: "upload_csv", rowCount: rows.length, fieldCount: columns.length, fieldProfiles: fp,
  });

  const dict = autoTagDictionary(workspace.id, dataset.id);
  assert.equal(dict.length, columns.length);

  const seg = createDefaultSegment(workspace.id, dataset.id);
  assert.equal(seg.name, "全样本（自动）");

  let capturedPrompt = "";
  const result = await runCrowdProfileGeneration(
    { segmentId: seg.id, model: "ep-smoke", businessContext: "" },
    {
      workspaceId: workspace.id,
      runPi: async ({ text }) => {
        capturedPrompt = text;
        return JSON.stringify({
          traits: ["t"], motivations: ["m"], decisionTriggers: ["d"], objections: ["o"],
          tone: "neutral", contentPreference: ["c"], riskNotes: ["r"], evidenceSummary: ["e"], persona: "p",
        });
      },
    },
  );
  assert.equal(result.profile.segmentId, seg.id);

  // 安全：prompt 不能含 PII / raw row 标识符
  for (const evil of ["user_id", "phone", "email", "openid", "unionid", "身份证", "手机号"]) {
    assert.ok(!capturedPrompt.includes(evil), `prompt 不含 PII 标记: "${evil}"`);
  }
});

