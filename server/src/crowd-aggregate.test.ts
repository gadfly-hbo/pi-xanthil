test("importAggregateDataset creates dataset + N segments from v2 wide-table", () => {
  const result = importAggregateDataset(workspace.id, "v2-test", V2_COLUMNS, V2_ROWS);

  assert.ok(result.dataset.id);
  assert.equal(result.dataset.source, "aggregate_upload");
  assert.equal(result.dataset.isAggregate, true);
  assert.equal(result.dataset.fieldCount, 26); // 27 - segment_name = 26 data columns

  // Should create 3 segments (one per row)
  assert.equal(result.segments.length, 3, "one segment per row");
  assert.equal(result.segments[0]!.name, "都市新锐女性");
  assert.equal(result.segments[1]!.name, "下沉市场实用派");
  assert.equal(result.segments[2]!.name, "高净值成熟客");

  // All segments are autoGenerated
  for (const seg of result.segments) {
    assert.equal(seg.autoGenerated, true);
    assert.equal(seg.sampleCount > 0, true);
  }

  // Tag dictionary: one entry per data column
  assert.ok(result.tagDictionary.length > 0);
  for (const entry of result.tagDictionary) {
    assert.equal(entry.autoGenerated, true);
  }
});

