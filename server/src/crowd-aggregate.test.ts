test("E2E: demo-detail.csv → computeFieldProfiles(normalizeEnums:true) → English enums", () => {
  const csvPath = join(process.cwd(), "server/static/crowd/demo-detail.csv");
  let csv: string;
  try {
    csv = readFileSync(csvPath, "utf-8");
  } catch {
    return; // skip if file not found (CI)
  }
  const lines = csv.trim().split("\n").filter((l) => l.trim());
  const columns = lines[0]!.split(",").map((c) => c.trim().replace(/^\uFEFF/, ""));
  const rows = lines.slice(1).map((line) => {
    const vals = line.split(",");
    const row: Record<string, unknown> = {};
    for (let i = 0; i < columns.length; i++) {
      row[columns[i]!] = vals[i]?.trim() ?? "";
    }
    return row;
  });

  const fp = computeFieldProfiles({ columns, rows, normalizeEnums: true });

  const genderFp = fp.find((p) => p.field === "gender")!;
  const genderValues = genderFp.topValues.map((v) => v.value);
  assert.ok(genderValues.includes("F"), "gender contains F");
  assert.ok(genderValues.includes("M"), "gender contains M");
  assert.ok(genderValues.includes("O"), "gender contains O");
  assert.ok(!genderValues.some((v) => /[男女其他]/.test(v)), "gender has no Chinese values");

  const cityFp = fp.find((p) => p.field === "city_tier")!;
  const cityValues = cityFp.topValues.map((v) => v.value);
  assert.ok(cityValues.includes("T1"), "city_tier contains T1");
  assert.ok(cityValues.includes("T1_NEW"), "city_tier contains T1_NEW");
  assert.ok(!cityValues.some((v) => /线/.test(v)), "city_tier has no Chinese values");

  const channelFp = fp.find((p) => p.field === "preferred_channel")!;
  const channelValues = channelFp.topValues.map((v) => v.value);
  assert.ok(channelValues.includes("xhs"), "channel contains xhs");
  assert.ok(channelValues.includes("wechat_mp"), "channel contains wechat_mp");
  assert.ok(!channelValues.some((v) => /[小红书抖音微信天猫京东线下]/.test(v)), "channel has no Chinese values");

  const lifecycleFp = fp.find((p) => p.field === "lifecycle_stage")!;
  const lifecycleValues = lifecycleFp.topValues.map((v) => v.value);
  assert.ok(lifecycleValues.includes("churned"), "lifecycle contains churned");
  assert.ok(lifecycleValues.includes("active"), "lifecycle contains active");
  assert.ok(!lifecycleValues.some((v) => /[新客活跃复购沉睡流失]/.test(v)), "lifecycle has no Chinese values");
});
